// Generates src/lib/tokenStats.ts — static per-token counts across the catalog
// so the questionnaire/archetype engine can IDF-weight rare tokens CLIENT-SIDE
// without ever importing catalog.json. Re-run after any catalog change:
//
//   node scripts/gen-token-stats.mjs
//
// Output is a plain data module (no catalog import) safe for client bundles.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const catalog = JSON.parse(fs.readFileSync(path.join(root, "src/data/catalog.json"), "utf8"));
const artists = catalog.artists;
const N = artists.length;

// Count how many idols carry each token, per field.
function counter() { return {}; }
const fields = {
  energy_type: counter(),
  fan_interaction: counter(),
  dance_style: counter(),
  content_tone: counter(),
  lifestyle_topics: counter(),
  value_topics: counter(),
  style_tags: counter(),     // includes official/personal style tags
  color_palette: counter(),
};

const bump = (map, tok) => { if (tok) map[tok] = (map[tok] ?? 0) + 1; };

for (const a of artists) {
  const p = a.profile;
  if (!p) continue;
  const pe = p.personality ?? {};
  const pf = p.performance ?? {};
  const c = p.content ?? {};
  const ae = p.aesthetic ?? {};
  bump(fields.energy_type, pe.energy_type);
  bump(fields.fan_interaction, pe.fan_interaction);
  bump(fields.dance_style, pf.dance_style);
  bump(fields.content_tone, c.content_tone);
  for (const t of c.lifestyle_topics ?? []) bump(fields.lifestyle_topics, t);
  for (const t of c.value_topics ?? []) bump(fields.value_topics, t);
  const styleAll = [
    ...(ae.style_tags ?? []),
    ...(ae.official?.style_tags ?? []),
    ...(ae.personal?.style_tags ?? []),
  ];
  for (const t of styleAll) bump(fields.style_tags, t);
  for (const t of ae.color_palette ?? []) bump(fields.color_palette, t);
}

// Sort each field's tokens by descending count for readability.
const sorted = {};
for (const [k, m] of Object.entries(fields)) {
  sorted[k] = Object.fromEntries(Object.entries(m).sort((a, b) => b[1] - a[1]));
}

const banner = `// AUTO-GENERATED by scripts/gen-token-stats.mjs — DO NOT EDIT BY HAND.
// Per-token idol counts across the ${N}-idol catalog, used for IDF rarity
// weighting in the questionnaire/archetype engine. Re-run the script after
// any catalog change. Safe for client bundles (no catalog import).
`;

const body = `${banner}
export const CATALOG_SIZE = ${N};

export type TokenCounts = Record<string, number>;

export const TOKEN_COUNTS: Record<string, TokenCounts> = ${JSON.stringify(sorted, null, 2)};

/**
 * IDF-style rarity weight for a token within a field: log(N / count(token)).
 * Rare tokens (e.g. mysterious 17/${N}) weigh far more than near-universal
 * ones (真誠待粉 212/${N}). Unknown tokens get the max weight (treated as rare).
 */
export function tokenWeight(field: string, token: string): number {
  const counts = TOKEN_COUNTS[field];
  const c = counts?.[token] ?? 1;
  return Math.log(CATALOG_SIZE / Math.max(1, c));
}
`;

fs.writeFileSync(path.join(root, "src/lib/tokenStats.ts"), body, "utf8");
console.log(`wrote src/lib/tokenStats.ts — ${N} idols`);
for (const [k, m] of Object.entries(sorted)) {
  console.log(`  ${k}: ${Object.keys(m).length} distinct`);
}
